# 2. Variables and Data Types
name = "Python Student"  # str
age = 20                # int
price = 99.99           # float
is_active = True        # bool

# 3. Printing values
print("Name:", name)
print("Age:", age)
print("Price:", price)
print("Is Active:", is_active)

# 4. Checking types
print("\nVariable Types:")
print("Type of name:", type(name))
print("Type of age:", type(age))
print("Type of price:", type(price))
print("Type of is_active:", type(is_active))

# 5. Simple Math with variables
a = 10
b = 5
sum_result = a + b
print("\nSum of", a, "and", b, "is:", sum_result)
